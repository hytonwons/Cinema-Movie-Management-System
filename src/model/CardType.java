package model;

public class CardType {
	private final static String SILVER ="银卡";
	private final static String GOLD ="金卡";
	private final static String DIAMOND ="钻石卡";
	
	public String getSilver() {
		return SILVER;
	}
	public String getGold() {
		return GOLD;
	}
	public String getDiamond() {
		return DIAMOND;
	}
}
